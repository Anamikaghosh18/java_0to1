class Variables{
    public static void main(String[] args) {
        int num1 = 10_000_1000; 
        System.out.println(num1);

        double num2 = 12e10;  // exponent 12 * 10^10
        System.out.println(num2);

        char c = 'a'; 
        System.out.println(c);
        c++;
        System.out.println(c);

    }
}